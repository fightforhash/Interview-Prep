#include <iostream>
using namespace std;

int main(){
    int n, m;
    cout << "두 개의 수를 입력하시오. " ;
    cin >> n >> m;
    
    cout << n << " + " << m << " = " << n + m << '\n';
    cout << n << " - " << m << " = " << n - m << '\n';
    cout << n << " * " << m << " = " << n * m << '\n';
    cout << n << " / " << m << " = " << n / m << '\n';
    cout << n << " % " << m << " = " << n % m << '\n';

}